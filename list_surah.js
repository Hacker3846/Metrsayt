/**
 * Возвращает название суры по её номеру
 * @param {number|string} surahNumber - Номер суры (от 1 до 114)
 * @returns {string} - Название суры или "Неизвестная сура"
 */
function getSurahName(surahNumber) {
    const num = parseInt(surahNumber, 10);
    return surahsList[num] || "Неизвестная сура";
}

/**
 * Преобразует номер суры в формат с нулями спереди (например: 9 -> "009", 89 -> "089")
 * Это необходимо для корректного составления URL к файлам на GitHub
 * @param {number|string} surahNumber - Исходный номер
 * @returns {string} - Форматированная строка из 3 цифр
 */
function formatSurahNumber(surahNumber) {
    const num = parseInt(surahNumber, 10);
    if (isNaN(num)) return "000";
    return String(num).padStart(3, '0');
}

